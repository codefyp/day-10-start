def format_name(f_name, l_name):
    formated_f_name = f_name.title()
    formated_l_name = l_name.title()
    #1 print(f"{formated_f_name} {formated_l_name}")
    return f"{formated_f_name} {formated_l_name}" #return is the end of the function and nothing can be executed after return
